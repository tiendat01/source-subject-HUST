--10. Tạo bảng TRANDAU_DatNT
CREATE TABLE TRANDAU_DatNT (
	MATRAN_4242 NUMERIC(18,0) IDENTITY(1,1) NOT NULL PRIMARY KEY,
	NAM_4242 INT NOT NULL,
	VONG_4242 INT NOT NULL,
	NGAYTD_4242 DATETIME NOT NULL,
	MACLB1 VARCHAR(5) NOT NULL,
	MACLB2 VARCHAR(5) NOT NULL,
	MASAN_4242 VARCHAR(5) NOT NULL,
	KETQUA_4242 VARCHAR(5) NOT NULL
